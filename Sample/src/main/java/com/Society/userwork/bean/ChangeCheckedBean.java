package com.Society.userwork.bean;

public class ChangeCheckedBean {
	private String checked;
	private String pay_status;
	private String date;
	private String time;
	private String apt_id;
	private String amount;
	private String mail_sent;
	
	public String getMail_sent() {
		return mail_sent;
	}
	public void setMail_sent(String mail_sent) {
		this.mail_sent = mail_sent;
	}
	public String getChecked() {
		return checked;
	}
	public void setChecked(String checked) {
		this.checked = checked;
	}
	public String getPay_status() {
		return pay_status;
	}
	public void setPay_status(String pay_status) {
		this.pay_status = pay_status;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getApt_id() {
		return apt_id;
	}
	public void setApt_id(String apt_id) {
		this.apt_id = apt_id;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	

}
